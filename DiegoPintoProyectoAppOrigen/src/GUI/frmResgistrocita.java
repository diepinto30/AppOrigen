    @SuppressWarnings("empty-statement")
    public void CargavaloresDoctores() {

        DefaultTableModel modelo;
        Object columnas[] = {"Nombres", "Especialidad", "Consultorio"};
        modelo = new DefaultTableModel(null, columnas);
        jTable1.setModel(modelo);
        try {
            for (Doctor doctor : Archivo()){
                String valor[] = {doctor.getStrNombre(), doctor.getStrEspecialidad(), doctor.getStrNumeroConsultorio()};
                modelo.addRow(valor);
            }

        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            String msn = ex.getLocalizedMessage();
        }
   
    }
    
    
    public ArrayList<Doctor> Archivo() {//Recupera una y otra vez los datos del archivo.
        Archivo objArchivo = new Archivo();
        ArrayList<Doctor> lstDoctor = new ArrayList<>();
        lstDoctor = objArchivo.LeerArchivoDoctor();
        return lstDoctor;
    
    }// </editor-fold> 
    
