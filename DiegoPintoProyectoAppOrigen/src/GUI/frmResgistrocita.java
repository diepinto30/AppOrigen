    // <editor-fold defaultstate="collapsed" desc="Validar y guardar datos">  
    public void Validar() {
        int intFecha = txtFecha.getText().length();
        int intHora = txtHora.getText().length();
        int intPacientes = txtPaciente.getText().length();
        int intDoctor = txtDoctor.getText().length();
        Archivo objArchivo = new Archivo();
        
        if ((intFecha == 0) || (intHora == 0) || (intPacientes == 0) || (intDoctor == 0)) {
            JOptionPane.showMessageDialog(null, "ERROR, NO ESTAN LLENOS TODO LOS REQUISITOS");
        }else{
            JOptionPane.showMessageDialog(null, "La cita a sido Registrada");
            objArchivo.AgregarRegistroCita(txtId.getText(), txtFecha.getText(), txtHora.getText(), txtDoctor.getText(), txtConsultorio.getText(), txtPaciente.getText());
        }   limpiar();
    }// </editor-fold> 

   

